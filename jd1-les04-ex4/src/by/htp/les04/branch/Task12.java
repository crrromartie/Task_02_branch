package by.htp.les04.branch;

public class Task12 {

	public static void task() {
		double x1 = 5.0;
		double x2 = -2.0;
		double x3 = -4.0;

		if (x1 >= 0) {
			System.out.println(Math.pow(x1, 2));
		}else {
			System.out.println(Math.pow(x1, 4));
		}
		
		if (x2 >= 0) {
			System.out.println(Math.pow(x2, 2));
		}else {
			System.out.println(Math.pow(x2, 4));
		}
		
		if (x3 >= 0) {
			System.out.println(Math.pow(x3, 2));
		}else {
			System.out.println(Math.pow(x3, 4));
		}
		
	}

}
